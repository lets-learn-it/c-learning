--[[
  > < >= <= == ~= (not equal)
  ]]
local a = 10; local b = 34; local c = 10

print("a > b =", (a > b))
print("a > c =", (a > c))

print("a < b =", (a < b))
print("a < c =", (a < c))

print("a >= b =", (a >= b))
print("a >= c =", (a >= c))

print("a == b =", (a == b))
print("a == c =", (a == c))

print("a ~= b =", (a ~= b))
print("a ~= c =", (a ~= c))

